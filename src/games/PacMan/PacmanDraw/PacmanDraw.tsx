function dup(arr: number[][]) {
    return arr.map(x => x.map(e => e))
}

function color(num: number) {
    let color = ''
    switch (num) {
        case 0:
            color = 'black'
            break
        case 1:
            color = 'blue'
            break
        case 2:
            color = 'yellow'
            break
        case 3:
            color = 'red'
            break
    }
    return color
}

export default function PacmanDraw() {
    const [board, setBoard] = useState<number[][]>(initialMap)
    return (
        <GameWrapper>
            <GameBoard>
                {board.map((x, l) => x.map((_, c) =>
                    <button
                        style={{ width: `${100 / 21}%`, height: `${100 / 27}%`, backgroundColor: color(board[l][c]) }}
                        onClick={() => {
                            let newBoard = dup(board)
                            newBoard[l][c] = (newBoard[l][c] + 1) % 4
                            setBoard(newBoard)
                        }}
                    />))}
            </GameBoard>
            <GameSideBar>
                <button onClick={() => console.log(`[${board.map(l => `\n[${l.toString()}]`)}]`)}>Print</button>
            </GameSideBar>
        </GameWrapper>
    )
}
